#Numero inteir
numero_inteiro = 3
print("Numero inteiro: ", numero_inteiro)

#Numero float
numero_float = 3.57
print("Numero FLOAT ", numero_float)

#Funcao type()
print("TYPE NUMERO INTEIRO: ", type(numero_inteiro))
print("TYPE NUMERO FLOAT: ", type(numero_float))

#SOMA
soma = 1 + 1
print("SOMA: ",soma)

#SUBTRACAO
subtracao = 1 - 1
print("SUBTRACAO: ",subtracao)

#MULTIPLICACAO
multiplicacao = 1 * 1
print("MULTIPLICACAO: ",multiplicacao)

#DIVISAO - sempre retorna float
divisao = 1 / 1
print("DIVISAO: ",divisao)
print("TIPO VAR DA DIVISAO: ",type(divisao))

#int()
print("PARSE PARA INT: ",int(divisao))

#float
numero_inteiro = 3
print("PARSE COM FLOAT ", float(numero_inteiro))

#MODULO % - restante da divisao
modulo = 5 % 2
print("RESTO DA DIVISAO ", modulo)

#DIVISAO COM O VALOR INTEIRO APENAS //
divisao_com_calor_inteiro = 5 // 2
print("DIVISAO MODO INTEIRO: ", divisao_com_calor_inteiro)

